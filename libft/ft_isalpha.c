int	ft_isalpha(char *str)
{
	int		i;
	char	c;

	i = 0;
	c = *(str + i);
	while (c)
	{
		if (!((c >= '0' && c <= '9') || c == '-' || c == '+'))
			return (1);
		c = *(str + (++i));
	}
	return (0);
}
